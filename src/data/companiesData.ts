export function getTechRouteMain(route: string): string {
  if (route.includes('侵入式')) return '侵入式';
  if (route.includes('非侵入式')) return '非侵入式';
  if (route.includes('介入式')) return '介入式';
  if (route.includes('神经调控')) return '神经调控';
  if (route.includes('fNIRS')) return 'fNIRS';
  if (route.includes('超声波')) return '超声波';
  if (route.includes('芯片')) return '芯片/传感器';
  if (route.includes('传感器')) return '芯片/传感器';
  if (route.includes('材料')) return '材料';
  if (route.includes('投资')) return '投资机构';
  return '其他';
}

export function getFundingDisplay(amount: number | null): string {
  if (amount === null || amount === undefined) return '-';
  if (amount >= 1) return `${amount}亿元`;
  return `${(amount * 10000).toFixed(0)}万元`;
}